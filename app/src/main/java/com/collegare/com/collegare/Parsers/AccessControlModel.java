
class AccessControlModel {

	public String commentVote;
	public String rate;
	public String syndicate;
	public String videoRespond;
	public String embed;
	public String list;
	public String comment;

	public AccessControlModel(String commentVote, String rate, String syndicate, String videoRespond, String embed, String list, String comment) {

		this.commentVote = commentVote;
		this.rate = rate;
		this.syndicate = syndicate;
		this.videoRespond = videoRespond;
		this.embed = embed;
		this.list = list;
		this.comment = comment;

	}

}