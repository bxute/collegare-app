
class ItemsModel {

	public String surname;
	public String fullname;
	public int index_start_at;
	public String email;
	public String name;
	public Boolean bool;
	public int integer;
	public int index;

	public ItemsModel(String surname, String fullname, int index_start_at, String email, String name, Boolean bool, int integer, int index) {

		this.surname = surname;
		this.fullname = fullname;
		this.index_start_at = index_start_at;
		this.email = email;
		this.name = name;
		this.bool = bool;
		this.integer = integer;
		this.index = index;

	}

}