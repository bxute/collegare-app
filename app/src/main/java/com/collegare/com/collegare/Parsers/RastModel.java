
class RastModel {

	public String type;
	public String created_time;
	public String updated_time;
	public String name;
	public String link;

	public RastModel(String type, String created_time, String updated_time, String name, String link) {

		this.type = type;
		this.created_time = created_time;
		this.updated_time = updated_time;
		this.name = name;
		this.link = link;

	}

}