
class RastModel {

	public String type;
	public String name;
	public String created_time;
	public String link;
	public String updated_time;

	public RastModel(String type, String name, String created_time, String link, String updated_time) {

		this.type = type;
		this.name = name;
		this.created_time = created_time;
		this.link = link;
		this.updated_time = updated_time;

	}

}