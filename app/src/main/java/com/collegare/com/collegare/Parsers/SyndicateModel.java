
class SyndicateModel {

	public String reason;
	public String title;
	public int duration;
	public String aspectRatio;

	public SyndicateModel(String reason, String title, int duration, String aspectRatio) {

		this.reason = reason;
		this.title = title;
		this.duration = duration;
		this.aspectRatio = aspectRatio;

	}

}