
class SyndicateModel {

	public int duration;
	public String reason;
	public String aspectRatio;
	public String title;

	public SyndicateModel(int duration, String reason, String aspectRatio, String title) {

		this.duration = duration;
		this.reason = reason;
		this.aspectRatio = aspectRatio;
		this.title = title;

	}

}