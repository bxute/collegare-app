
class SyndicateModel {

	public String aspectRatio;
	public String title;
	public int duration;
	public String reason;

	public SyndicateModel(String aspectRatio, String title, int duration, String reason) {

		this.aspectRatio = aspectRatio;
		this.title = title;
		this.duration = duration;
		this.reason = reason;

	}

}