
class SyndicateModel {

	public String aspectRatio;
	public String title;
	public String reason;
	public int duration;

	public SyndicateModel(String aspectRatio, String title, String reason, int duration) {

		this.aspectRatio = aspectRatio;
		this.title = title;
		this.reason = reason;
		this.duration = duration;

	}

}