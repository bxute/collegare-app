
class VersioningModel {

	public int eventver;
	public String timezone;
	public String enter;
	public int trsver;
	public String startdate;
	public String privacy;

	public VersioningModel(int eventver, String timezone, String enter, int trsver, String startdate, String privacy) {

		this.eventver = eventver;
		this.timezone = timezone;
		this.enter = enter;
		this.trsver = trsver;
		this.startdate = startdate;
		this.privacy = privacy;

	}

}