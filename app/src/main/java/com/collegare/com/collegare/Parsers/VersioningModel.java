
class VersioningModel {

    public String privacy;
    public int trsver;
    public String enter;
    public int eventver;
    public String timezone;
    public String startdate;

    public VersioningModel(String privacy, int trsver, String enter, int eventver, String timezone, String startdate) {

        this.privacy = privacy;
        this.trsver = trsver;
        this.enter = enter;
        this.eventver = eventver;
        this.timezone = timezone;
        this.startdate = startdate;

    }

}