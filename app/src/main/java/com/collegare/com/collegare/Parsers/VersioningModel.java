
class VersioningModel {

	public String privacy;
	public int trsver;
	public String startdate;
	public String timezone;
	public int eventver;
	public String enter;

	public VersioningModel(String privacy, int trsver, String startdate, String timezone, int eventver, String enter) {

		this.privacy = privacy;
		this.trsver = trsver;
		this.startdate = startdate;
		this.timezone = timezone;
		this.eventver = eventver;
		this.enter = enter;

	}

}