
class VersioningModel {

	public String privacy;
	public String enter;
	public int eventver;
	public String timezone;
	public String startdate;
	public int trsver;

	public VersioningModel(String privacy, String enter, int eventver, String timezone, String startdate, int trsver) {

		this.privacy = privacy;
		this.enter = enter;
		this.eventver = eventver;
		this.timezone = timezone;
		this.startdate = startdate;
		this.trsver = trsver;

	}

}