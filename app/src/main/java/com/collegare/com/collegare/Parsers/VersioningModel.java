
class VersioningModel {

	public String privacy;
	public int trsver;
	public String startdate;
	public String timezone;
	public String enter;
	public int eventver;

	public VersioningModel(String privacy, int trsver, String startdate, String timezone, String enter, int eventver) {

		this.privacy = privacy;
		this.trsver = trsver;
		this.startdate = startdate;
		this.timezone = timezone;
		this.enter = enter;
		this.eventver = eventver;

	}

}