
class AddressModel {

	public String streetAddress;
	public String city;
	public String state;
	public String postalCode;

	public AddressModel(String streetAddress, String city, String state, String postalCode) {

		this.streetAddress = streetAddress;
		this.city = city;
		this.state = state;
		this.postalCode = postalCode;

	}

}