
class AddressModel {

	public String streetAddress;
	public String postalCode;
	public String city;
	public String state;

	public AddressModel(String streetAddress, String postalCode, String city, String state) {

		this.streetAddress = streetAddress;
		this.postalCode = postalCode;
		this.city = city;
		this.state = state;

	}

}