
class AddressModel {

	public String state;
	public String postalCode;
	public String city;
	public String streetAddress;

	public AddressModel(String state, String postalCode, String city, String streetAddress) {

		this.state = state;
		this.postalCode = postalCode;
		this.city = city;
		this.streetAddress = streetAddress;

	}

}