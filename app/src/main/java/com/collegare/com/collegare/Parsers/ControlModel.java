
class ControlModel {

	public String rate;
	public String aspectRatio;
	public String list;
	public String videoRespond;
	public String comment;
	public int viewCount;
	public String embed;
	public int favoriteCount;
	public int ratingCount;
	public String commentVote;
	public String syndicate;

	public ControlModel(String rate, String aspectRatio, String list, String videoRespond, String comment, int viewCount, String embed, int favoriteCount, int ratingCount, String commentVote, String syndicate) {

		this.rate = rate;
		this.aspectRatio = aspectRatio;
		this.list = list;
		this.videoRespond = videoRespond;
		this.comment = comment;
		this.viewCount = viewCount;
		this.embed = embed;
		this.favoriteCount = favoriteCount;
		this.ratingCount = ratingCount;
		this.commentVote = commentVote;
		this.syndicate = syndicate;

	}

}