import java.util.ArrayList;

class ContainerModel {

    public String desc;
    public String day;
    public String udecs;
    public String post;
    public String remedy;
    public String updateded;
    public String aperson;
    public ArrayList<String> tags;
    public String uploadedon;
    public String updatedon;

    public ContainerModel(String desc, String day, String udecs, String post, String remedy, String updateded, String aperson, ArrayList<String> tags, String uploadedon, String updatedon) {

        this.desc = desc;
        this.day = day;
        this.udecs = udecs;
        this.post = post;
        this.remedy = remedy;
        this.updateded = updateded;
        this.aperson = aperson;
        this.tags = tags;
        this.uploadedon = uploadedon;
        this.updatedon = updatedon;

    }

}